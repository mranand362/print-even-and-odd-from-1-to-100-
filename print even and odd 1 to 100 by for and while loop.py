#to print even  number from 1 to 100 using "for" loop
for x in range(2,100,2):
    print(x )



# to print odd number from 1 to 100 using "for" loop
for x in range(1,100,2):
    print(x )


# to print all even number fromn 1 to 100 using "while loop"
x= 2
while x<=100:
  print(x , end = " ")
  x = x+2



